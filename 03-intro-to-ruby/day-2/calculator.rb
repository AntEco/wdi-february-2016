class Elevator
  def self.add(operand_1, operand_2)
    operand_1 + operand_2
  end

  def self.subtract(operand_1, operand_2)
    operand_1 - operand_2
  end

  def self.multiply(operand_1, operand_2)
    operand_1 * operand_2
  end

  def self.divide(operand_1, operand_2)
    operand_1 / operand_2
  end
end
